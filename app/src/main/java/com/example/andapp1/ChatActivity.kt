            } catch (e: Exception) {
                Log.e("OCR_CAMERA", "‚ùå Ïù¥ÎØ∏ÏßÄ ÎîîÏΩîÎî© Ï§ë Ïò§Î•ò Î∞úÏÉù: ${e.message}")
            }
