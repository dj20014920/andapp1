    private fun processOcrWithPeopleInput(bitmap: Bitmap) {
        val participantsRef = FirebaseDatabase.getInstance()
            .getReference("rooms")
            .child(viewModel.roomCode)
            .child("participants")

        participantsRef.get().addOnSuccessListener { snapshot ->
            val defaultPeople = snapshot.childrenCount.toInt().coerceAtLeast(1)

            val editText = EditText(this).apply {
                setText(defaultPeople.toString())
                inputType = InputType.TYPE_CLASS_NUMBER
            }

            AlertDialog.Builder(this)
                .setTitle("Ï†ïÏÇ∞ Ïù∏Ïõê ÏàòÎ•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî")
                .setMessage("Í∏∞Î≥∏Í∞íÏùÄ ${defaultPeople}Î™ÖÏûÖÎãàÎã§.")
                .setView(editText)
                // processOcrWithPeopleInput() ÏïàÏóêÏÑú‚Ä¶
                .setPositiveButton("ÌôïÏù∏") { _, _ ->
                    val people = editText.text.toString().toIntOrNull() ?: defaultPeople
                    try {
                        val text = ReceiptOcrProcessor.processReceipt(this, bitmap)
                        Log.d("OCR", "ChatActivity ‚Üí OCR Í≤∞Í≥º ÌÖçÏä§Ìä∏ = $text")
                        val total = ReceiptOcrProcessor.extractTotalAmount(text)
                        Log.d("OCR", "ChatActivity ‚Üí total = $total, people = $people")
                        val message = "‚Üí Ï¥ùÌï©: ${total ?: 0}Ïõê / Ïù∏Îãπ: ${(total ?: 0) / people}Ïõê"
                        sendChatMessage(message)

                    } catch (e: Exception) {
                        Log.e("OCR_PROCESS", "ChatActivity OCR Ï≤òÎ¶¨ Ï§ë ÏòàÏô∏", e)
                        Toast.makeText(this, "ÏòÅÏàòÏ¶ù Ïù∏ÏãùÏóê Ïã§Ìå®ÌñàÏäµÎãàÎã§.", Toast.LENGTH_SHORT).show()
                    }
                }

                .setNegativeButton("Ï∑®ÏÜå", null)
                .show()
        }
    }
    
    private fun uploadImageToFirebase(uri: Uri) {
        val fileName = "images/${System.currentTimeMillis()}.jpg"
        val storageRef = FirebaseStorage.getInstance().reference.child(fileName)

        Log.d("PHOTO", "ÏóÖÎ°úÎìú ÏãúÎèÑ URI: $uri")

        storageRef.putFile(uri)
            .addOnSuccessListener {
                Log.d("PHOTO", "‚úÖ ÏóÖÎ°úÎìú ÏÑ±Í≥µ")
                storageRef.downloadUrl.addOnSuccessListener { downloadUrl ->
                    Log.d("PHOTO", "‚úÖ Îã§Ïö¥Î°úÎìú URL: $downloadUrl")
                    sendImageMessage(downloadUrl.toString()) // Ïù¥Îïå imageUrlValueÎ°ú ÎÑ£Ïñ¥Ïïº Ìï®
                }
            }
            .addOnFailureListener { e ->
                Log.e("PHOTO", "‚ùå ÏóÖÎ°úÎìú Ïã§Ìå®: ${e.message}")
                e.printStackTrace()
                Toast.makeText(this, "ÏÇ¨ÏßÑ ÏóÖÎ°úÎìú Ïã§Ìå®", Toast.LENGTH_SHORT).show()
            }
    }

    private fun sendChatMessage(message: String) {
        viewModel.sendMessage(message)
    }

    private fun sendImageMessage(imageUrl: String) {
        Log.d("ChatActivity", "Sending image message: $imageUrl")

        val user = currentUser ?: return
        val author = Author(user.id, user.nickname ?: "Ïïå Ïàò ÏóÜÏùå", null)

        val message = ChatMessage(
            id = System.currentTimeMillis().toString(),
            text = "",
            user = author,
            imageUrlValue = imageUrl,
            createdAt = Date()
        )

        Log.d("üîç ChatDebug", "adapter senderId = $senderId") // ÏßÅÏ†ë senderIdÎ•º Ï†ÄÏû•Ìï¥ÎëêÏóàÎã§Î©¥
        Log.d("üîç ChatDebug", "message sender id = ${message.getUser().getId()}")

        viewModel.sendMessage(message)
    }

